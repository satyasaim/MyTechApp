package com.example.myapplicationtechapp.Pojodata;

public class Pojoclass {
    public String name;
    public String cources;
    public String images;
    public String titel_id;

    public Pojoclass() {
        this.name = name;
        this.cources = cources;
        this.images = images;
        this.titel_id=titel_id;
    }



    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCources() {
        return cources;
    }

    public void setCources(String cources) {
        this.cources = cources;
    }

    public String getImages() {
        return images;
    }

    public void setImages(String images) {
        this.images = images;
    }

    public String getTitel_id() {
        return titel_id;
    }

    public void setTitel_id(String titel_id) {
        this.titel_id = titel_id;
    }
}
